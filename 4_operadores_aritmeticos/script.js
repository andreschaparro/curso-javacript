// Operador de asignación
let variable = 'Valor de la variable'
console.log(variable)

// Operador aritmético de la suma
let suma = 5 + 2
console.log('5 + 2 =', suma)
// Operador aritmético de la resta
let resta = 5 - 2
console.log('5 - 2 =', resta)
// Operador aritmético de la multiplicación
let multiplicacion = 5 * 2
console.log('5 * 2 =', multiplicacion)
// Operador aritmético de la división
let division = 5 / 2
console.log('5 / 2 =', division)
// Operador aritmético del resto de la división
let resto = 5 % 2
console.log('5 % 2 =', resto)
// Operador aritmético de la exponenciación
let potencia = 5 ** 2
console.log('5 ** 2 =', potencia)

let x = 5;
console.log(x)
// Operador aumentador (incremento en 1)
x++
console.log(x)

let y = 5;
console.log(y)
// Operador disminuidor (decremento en 1)
y--
console.log(y)